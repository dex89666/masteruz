-- ============================================================================
-- MasterUz — 0029: уведомление клиента о зависшем заказе
-- ----------------------------------------------------------------------------
-- Авто-отмена заказов отключена намеренно (заказ живёт, пока клиент не отменит
-- сам). Побочный эффект: если мастер долго не находится, деньги клиента
-- заморожены в эскроу бессрочно, а клиент об этом не знает.
--
-- Флаг фиксирует момент, когда клиенту отправлено уведомление «мастер не
-- найден, деньги можно вернуть», чтобы часовой тик не рассылал его повторно.
-- ============================================================================

ALTER TABLE "orders" ADD COLUMN IF NOT EXISTS "client_stale_notified_at" TIMESTAMP(3);

CREATE INDEX IF NOT EXISTS "orders_stale_notify_idx"
  ON "orders" ("created_at")
  WHERE "status" = 'PUBLISHED' AND "master_id" IS NULL AND "client_stale_notified_at" IS NULL;
